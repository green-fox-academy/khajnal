public class Fox {
  String name;
  String type;
  String color;

  Fox(String name, String type, String color) {
    this.name = name;
    this.type = type;
    this.color = color;
  }

  public String getcolor() {
    return color;
  }

  public String getName() {
    return name;
  }

  public String getType() {
    return type;
  }
}